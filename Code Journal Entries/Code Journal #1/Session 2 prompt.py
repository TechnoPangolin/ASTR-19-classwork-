import numpy as np

def main():
	#sum of floats
	a = 10.0
	b = 4.0
	c = a + b
	print("sum is: ", c, " and type is: ", type(c))

	#diff of integers
	a = 10
	b = 4
	c = a - b
	print("difference is: ", c, " and type is: ", type(c))

	#product of float and int
	a = 10.0
	b = 4
	c = a * b
	print("product is: ", c, " and type is: ", type(c))

if __name__ == '__main__':
	main()